/**
 * Generate a Markdown validation annotation for a given section/index/field.
 * Returns a blockquote line (e.g. "> ⚠️ **uncertain:** ...") or empty string.
 */
function formatMdValidation(section, index, field, featureIndex) {
  if (typeof getValidationStatus === 'undefined') return '';
  const status = (featureIndex != null)
    ? getValidationStatus(section, index, field, featureIndex)
    : getValidationStatus(section, index, field);
  const detail = (typeof getValidationDetail !== 'undefined')
    ? ((featureIndex != null)
        ? getValidationDetail(section, index, field, featureIndex)
        : getValidationDetail(section, index, field))
    : null;
  if (!status) return '';

  if (status === 'likely_valid') return '> ✅ Source confirmed';
  const icon = status === 'uncertain' ? '⚠️' : '❌';
  const label = status === 'uncertain' ? 'uncertain' : 'flagged';
  let line = '> ' + icon + ' **' + label + ':** ' + (detail?.reason || '');
  if (detail?.suggested_fix) line += ' *→ ' + detail.suggested_fix + '*';
  return line;
}

/**
 * Generate a plain-text validation annotation for PPTX table cells.
 * Returns a newline-prefixed string or empty string.
 */
function pptxValidationText(section, index, field, featureIndex) {
  if (typeof getValidationStatus === 'undefined') return '';
  const status = (featureIndex != null)
    ? getValidationStatus(section, index, field, featureIndex)
    : getValidationStatus(section, index, field);
  const detail = (typeof getValidationDetail !== 'undefined')
    ? ((featureIndex != null)
        ? getValidationDetail(section, index, field, featureIndex)
        : getValidationDetail(section, index, field))
    : null;
  if (!status) return '';

  if (status === 'likely_valid') return '\n✅ confirmed';
  const icon = status === 'uncertain' ? '⚠️' : '❌';
  let text = '\n' + icon + ' ' + (detail?.reason || '');
  if (detail?.suggested_fix) text += '\n→ ' + detail.suggested_fix;
  return text;
}

/**
 * Build a combined Markdown document from both stages.
 */
function buildMarkdown(stage1, stage2, projectSlug) {
  const now = new Date();
  const dateStr = now.toISOString().slice(0, 10);
  const lines = [];

  lines.push('# Innovative Ideation Scan: ' + (projectSlug || 'Untitled'));
  lines.push('**Date:** ' + dateStr);
  lines.push('**Generated by:** AI Ideation Scanner (SDC AI Tools)');
  lines.push('**Status:** DRAFT — requires practitioner review before use');
  lines.push('');
  lines.push('---');
  lines.push('');

  // ── PART 1: Innovation Scan Insights ──
  lines.push('## Part 1: Innovation Scan Insights');
  lines.push('');
  lines.push('### Scan Summary');
  lines.push(stage1.scan_summary || '_No summary provided._');
  lines.push('');

  // Best Practices
  lines.push('### Best Practices');
  lines.push('');
  if (stage1.best_practices && stage1.best_practices.length > 0) {
    stage1.best_practices.forEach((bp, i) => {
      lines.push('#### ' + (i + 1) + '. ' + bp.title);
      const titleVal = formatMdValidation('best_practices', i, 'title');
      if (titleVal) lines.push(titleVal);
      lines.push('**Source:** ' + (bp.source_domain || 'N/A'));
      lines.push('');
      lines.push(bp.description || '');
      lines.push('');
      lines.push('**Relevance:** ' + (bp.relevance || 'N/A'));
      if (bp.url_hint) {
        lines.push('**Reference:** ' + formatMdLink(bp.url_hint));
        const urlVal = formatMdValidation('best_practices', i, 'url_hint');
        if (urlVal) lines.push(urlVal);
      }
      lines.push('');
    });
  } else {
    lines.push('_No best practices identified._');
    lines.push('');
  }

  // Analogous Solutions
  lines.push('### Analogous Solutions');
  lines.push('');
  if (stage1.analogous_solutions && stage1.analogous_solutions.length > 0) {
    stage1.analogous_solutions.forEach((as, i) => {
      lines.push('#### ' + (i + 1) + '. ' + as.title);
      const titleVal = formatMdValidation('analogous_solutions', i, 'title');
      if (titleVal) lines.push(titleVal);
      lines.push('**Sector:** ' + (as.sector || 'N/A'));
      lines.push('');
      lines.push(as.description || '');
      lines.push('');
      lines.push('**Transferable Insight:** ' + (as.transferable_insight || 'N/A'));
      if (as.url_hint) {
        lines.push('**Reference:** ' + formatMdLink(as.url_hint));
        const urlVal = formatMdValidation('analogous_solutions', i, 'url_hint');
        if (urlVal) lines.push(urlVal);
      }
      lines.push('');
    });
  } else {
    lines.push('_No analogous solutions identified._');
    lines.push('');
  }

  // Design Trends
  lines.push('### Design Trends');
  lines.push('');
  if (stage1.design_trends && stage1.design_trends.length > 0) {
    stage1.design_trends.forEach((dt, i) => {
      lines.push('**' + (i + 1) + '. ' + dt.trend + '** — ' + (dt.description || ''));
      const trendTitleVal = formatMdValidation('design_trends', i, 'title');
      if (trendTitleVal) lines.push(trendTitleVal);
      lines.push('*Application:* ' + (dt.application || 'N/A'));
      if (dt.url_hint) {
        lines.push('*Reference:* ' + formatMdLink(dt.url_hint));
        const urlVal = formatMdValidation('design_trends', i, 'url_hint');
        if (urlVal) lines.push(urlVal);
      }
      lines.push('');
    });
  } else {
    lines.push('_No design trends identified._');
    lines.push('');
  }

  // Inspiration Sources
  lines.push('### Inspiration Sources');
  lines.push('');
  if (stage1.inspiration_sources && stage1.inspiration_sources.length > 0) {
    stage1.inspiration_sources.forEach((is, i) => {
      let line = '- **' + is.name + '** (' + (is.type || 'Resource') + ') — ' + (is.why_relevant || '');
      if (is.url_hint) line += ' · ' + formatMdLink(is.url_hint);
      lines.push(line);
      const nameVal = formatMdValidation('inspiration_sources', i, 'title');
      if (nameVal) lines.push('  ' + nameVal);
      if (is.url_hint) {
        const urlVal = formatMdValidation('inspiration_sources', i, 'url_hint');
        if (urlVal) lines.push('  ' + urlVal);
      }
    });
    lines.push('');
  } else {
    lines.push('_No inspiration sources identified._');
    lines.push('');
  }

  lines.push('---');
  lines.push('');

  // ── PART 2: Unpacked Solution Concepts ──
  lines.push('## Part 2: Unpacked Solution Concepts');
  lines.push('');
  lines.push('### Solution Overview');
  lines.push(stage2.solution_overview || '_No overview provided._');
  lines.push('');

  // Solution Concepts
  if (stage2.solution_concepts && stage2.solution_concepts.length > 0) {
    stage2.solution_concepts.forEach((concept, ci) => {
      lines.push('### Concept ' + (ci + 1) + ': ' + concept.concept_name);
      const conceptTitleVal = formatMdValidation('solution_concepts', ci, 'title');
      if (conceptTitleVal) lines.push(conceptTitleVal);
      if (concept.opportunity_source) {
        lines.push('**Opportunity:** ' + concept.opportunity_source);
      }
      lines.push('');
      lines.push(concept.concept_description || '');
      lines.push('');

      if (concept.features && concept.features.length > 0) {
        lines.push('| # | Feature | Description | HCD Value | Best Practice Ref | Complexity |');
        lines.push('|---|---------|-------------|-----------|-------------------|------------|');
        const featureValNotes = [];
        concept.features.forEach((f, fi) => {
          const desc = (f.description || '').replace(/\n/g, ' ').replace(/\|/g, '\\|');
          const hcd = (f.hcd_value_proposition || '').replace(/\n/g, ' ').replace(/\|/g, '\\|');
          let ref = (f.best_practice_reference || '').replace(/\n/g, ' ').replace(/\|/g, '\\|');
          if (f.best_practice_url) {
            ref += ' (' + formatMdLink(f.best_practice_url).replace(/\|/g, '\\|') + ')';
          }
          const cx = f.implementation_complexity || 'N/A';
          lines.push('| ' + (fi + 1) + ' | **' + (f.feature_name || '') + '** | ' + desc + ' | ' + hcd + ' | ' + ref + ' | ' + cx + ' |');
          // Collect validation notes for features (can't embed in table cells)
          if (f.best_practice_url) {
            const fVal = formatMdValidation('solution_concepts', ci, 'best_practice_url', fi);
            if (fVal) featureValNotes.push('- **' + (f.feature_name || 'Feature ' + (fi + 1)) + ':** ' + fVal.replace(/^> /, ''));
          }
        });
        lines.push('');
        // Append feature validation notes after the table
        if (featureValNotes.length > 0) {
          lines.push('**Source Validation Notes:**');
          featureValNotes.forEach(n => lines.push(n));
          lines.push('');
        }
      }
    });
  }

  // Cross-Cutting Themes
  if (stage2.cross_cutting_themes && stage2.cross_cutting_themes.length > 0) {
    lines.push('### Cross-Cutting Themes');
    lines.push('');
    stage2.cross_cutting_themes.forEach((t, i) => {
      let line = '- **' + t.theme + ':** ' + (t.description || '');
      if (t.url_hint) line += ' · ' + formatMdLink(t.url_hint);
      lines.push(line);
      const themeVal = formatMdValidation('cross_cutting_themes', i, 'title');
      if (themeVal) lines.push('  ' + themeVal);
      if (t.url_hint) {
        const urlVal = formatMdValidation('cross_cutting_themes', i, 'url_hint');
        if (urlVal) lines.push('  ' + urlVal);
      }
    });
    lines.push('');
  }

  // Risks & Considerations
  if (stage2.risks_and_considerations && stage2.risks_and_considerations.length > 0) {
    lines.push('### Risks & Considerations');
    lines.push('');
    lines.push('| Risk | Mitigation |');
    lines.push('|------|------------|');
    stage2.risks_and_considerations.forEach((r) => {
      const risk = (r.risk || '').replace(/\|/g, '\\|');
      const mit = (r.mitigation || '').replace(/\|/g, '\\|');
      lines.push('| ' + risk + ' | ' + mit + ' |');
    });
    lines.push('');
  }

  // ── Source Validation Summary (if validation has been run) ──
  if (typeof lastValidation !== 'undefined' && lastValidation && lastValidation.flags) {
    lines.push('### Source Validation Summary');
    lines.push('');
    const flags = lastValidation.flags;
    const valid = flags.filter(f => f.status === 'likely_valid').length;
    const uncertain = flags.filter(f => f.status === 'uncertain').length;
    const invalid = flags.filter(f => f.status === 'likely_invalid').length;
    const score = lastValidation.confidence_score != null
      ? Math.round(lastValidation.confidence_score * 100) : null;
    lines.push('- ✅ ' + valid + ' confirmed · ⚠️ ' + uncertain + ' uncertain · ❌ ' + invalid + ' flagged');
    if (score !== null) lines.push('- **Confidence:** ' + score + '%');
    if (lastValidation.validation_summary) {
      lines.push('');
      lines.push(lastValidation.validation_summary);
    }
    lines.push('');
  }

  lines.push('---');
  lines.push('*AI-generated ideation scan. Practitioner review required before use in deliverables.*');

  return lines.join('\n');
}

/**
 * Format a url_hint as a Markdown link.
 * If it starts with http(s), render as [url](url).
 * Otherwise, render as a Google search link.
 */
function formatMdLink(urlHint) {
  if (!urlHint) return '';
  if (/^https?:\/\//i.test(urlHint)) {
    return '[' + urlHint + '](' + urlHint + ')';
  }
  const searchUrl = 'https://www.google.com/search?q=' + encodeURIComponent(urlHint);
  return '[🔎 ' + urlHint + '](' + searchUrl + ')';
}

/**
 * Download a Markdown file.
 */
function exportMarkdown(stage1, stage2, projectSlug) {
  const md = buildMarkdown(stage1, stage2, projectSlug);
  const dateStr = new Date().toISOString().slice(0, 10);
  const slug = (projectSlug || 'ideation-scan').replace(/\s+/g, '-').toLowerCase();
  const filename = dateStr + '-ideation-scan-' + slug + '.md';

  const blob = new Blob([md], { type: 'text/markdown;charset=utf-8' });
  downloadBlob(blob, filename);
}

/**
 * Download a DOCX file (Markdown → HTML → DOCX).
 */
function exportDocx(stage1, stage2, projectSlug) {
  const md = buildMarkdown(stage1, stage2, projectSlug);
  const dateStr = new Date().toISOString().slice(0, 10);
  const slug = (projectSlug || 'ideation-scan').replace(/\s+/g, '-').toLowerCase();
  const filename = dateStr + '-ideation-scan-' + slug + '.docx';

  // Convert markdown to HTML
  const html = typeof marked !== 'undefined'
    ? marked.parse(md)
    : '<pre>' + md.replace(/</g, '&lt;') + '</pre>';

  // Wrap in a basic document structure
  const fullHtml = '<!DOCTYPE html><html><head><meta charset="utf-8"><style>'
    + 'body{font-family:Calibri,sans-serif;font-size:11pt;line-height:1.5;}'
    + 'h1{font-size:18pt;color:#1a3a5c;} h2{font-size:14pt;color:#1a3a5c;} h3{font-size:12pt;color:#1a3a5c;}'
    + 'table{border-collapse:collapse;width:100%;margin:12px 0;} th,td{border:1px solid #d1d5db;padding:6px 10px;font-size:10pt;text-align:left;}'
    + 'th{background:#f3f4f6;font-weight:600;}'
    + 'a{color:#4A90D9;text-decoration:underline;}'
    + '</style></head><body>' + html + '</body></html>';

  if (typeof htmlDocx !== 'undefined') {
    const blob = htmlDocx.asBlob(fullHtml);
    downloadBlob(blob, filename);
  } else {
    // Fallback: download as HTML
    const blob = new Blob([fullHtml], { type: 'text/html;charset=utf-8' });
    downloadBlob(blob, filename.replace('.docx', '.html'));
  }
}

