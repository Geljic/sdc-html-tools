/**
 * Build a JSON template containing all inputs and results.
 * This allows saving and restoring a complete ideation scan session.
 */
function buildJsonTemplate(projectSlug, lastResult) {
  const context = (document.getElementById('project-context')?.value || '').trim();
  const opportunities = (document.getElementById('opportunities-text')?.value || '').trim();
  const scanFocus = document.getElementById('scan-focus')?.value || 'all';
  const otherFocusText = (document.getElementById('other-focus-text')?.value || '').trim();

  const template = {
    _tool: 'SDC Ideation Scanner',
    _version: '2.1',
    savedAt: new Date().toISOString(),
    projectSlug: projectSlug || '',
    projectContext: context,
    opportunitiesText: opportunities,
    scanFocus: scanFocus,
    otherFocusText: otherFocusText,
    uploadedFileNames: (typeof uploadedFiles !== 'undefined' ? uploadedFiles : []).map(f => f.name),
  };

  // Include results if a scan has been run
  if (lastResult) {
    template.results = {
      stage1: lastResult.stage1 || null,
      stage2: lastResult.stage2 || null,
      model: lastResult.model || '',
      elapsed: lastResult.elapsed || '',
      usage: lastResult.usage || null,
    };

    // Include validation token usage if available
    if (typeof cumulativeUsage !== 'undefined' && cumulativeUsage && cumulativeUsage.validationTokens) {
      template.results.validationUsage = cumulativeUsage.validationTokens;
    }
  }

  // Include validation results if validation has been run
  if (typeof lastValidation !== 'undefined' && lastValidation) {
    template.validation = lastValidation;
  }
  if (typeof validationMap !== 'undefined' && Object.keys(validationMap).length > 0) {
    template.validationMap = validationMap;
  }

  return template;
}

/**
 * Export the current session as a JSON file.
 */
function exportJSON(projectSlug, lastResult) {
  const template = buildJsonTemplate(projectSlug, lastResult);
  const dateStr = new Date().toISOString().slice(0, 10);
  const hh = String(new Date().getHours()).padStart(2, '0');
  const mm = String(new Date().getMinutes()).padStart(2, '0');
  const slug = (projectSlug || 'ideation-scan').replace(/\s+/g, '-').toLowerCase();
  const filename = 'ideation-scan-' + slug + '-' + dateStr + '-' + hh + mm + '.json';

  const blob = new Blob([JSON.stringify(template, null, 2)], { type: 'application/json' });
  downloadBlob(blob, filename);
  showToast('💾 Saved ' + filename);
}

/**
 * Import a JSON template file and restore the session.
 * @param {File} file - The JSON file to import
 * @param {function} onComplete - Callback with the parsed template
 */
function importJSON(file, onComplete) {
  if (!file || !file.name.endsWith('.json')) {
    showToast('❌ Please select a .json file exported from this tool.');
    return;
  }

  const reader = new FileReader();
  reader.onload = (ev) => {
    try {
      const parsed = JSON.parse(ev.target.result);

      // Basic validation — check it's from this tool
      if (parsed._tool && parsed._tool !== 'SDC Ideation Scanner') {
        showToast('⚠ This JSON file is from "' + parsed._tool + '", not the Ideation Scanner.');
        return;
      }

      if (onComplete) onComplete(parsed);
    } catch (err) {
      console.error('JSON import error:', err);
      showToast('❌ Could not parse JSON file: ' + err.message);
    }
  };
  reader.onerror = () => showToast('❌ Could not read file.');
  reader.readAsText(file);
}

/**
 * Show a toast notification.
 */
function showToast(message) {
  let toast = document.getElementById('toast');
  if (!toast) {
    toast = document.createElement('div');
    toast.id = 'toast';
    toast.className = 'toast';
    document.body.appendChild(toast);
  }
  toast.textContent = message;
  toast.classList.add('show');
  setTimeout(() => toast.classList.remove('show'), 2500);
}
